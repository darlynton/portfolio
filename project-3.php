                            <ul class="rest">
                                <li>Data-dense layouts: Tables, cards, and visualization components designed to display complex information without overwhelming users</li>
                                <li>Accessibility built-in: WCAG AA compliance ensures the platform is usable by auditors with diverse needs</li>
                                <li>Established patterns: Users familiar with IBM products can transfer their knowledge, reducing learning curve</li>
                                <li>Professional aesthetic: Clean, structured design communicates trust and credibility—essential for a governance tool</li>
