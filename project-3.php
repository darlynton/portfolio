                                <h6 class="sub-title mb-0">Reducing Cognitive Load</h6>
                            </div>
                            <p>Enterprise governance tools risk overwhelming users with information. Several design decisions specifically addressed cognitive load:<br><br></p>
                            <ul class="rest">
                                <li>Progressive disclosure: Dashboard shows summary metrics; clicking through reveals detailed analysis. Users aren't confronted with all data at once.</li>
                                <li>Visual hierarchy: Critical alerts use red, warnings use amber, normal status uses neutral colors—users can scan quickly for issues</li>
                                <li>Consistent patterns: Card layouts, table structures, and action buttons work the same way across all screens</li>
                                <li>Contextual actions: Buttons appear only when relevant (e.g., "Pause Model" only shows for active models)</li>
                                <li>Default views: Each screen loads with sensible defaults (last 30 days, current model version) to minimize decision fatigue</li>
                            </ul>
