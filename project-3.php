                                <h6 class="sub-title mb-0">Information Architecture Rationale</h6>
                            </div>
                            <p>The three-tier navigation structure (primary → detail → sub-tabs) was designed to balance discoverability with focus:<br><br></p>
                            <ul class="rest">
                                <li>Primary navigation represents user goals (monitor, audit, report) rather than technical concepts</li>
                                <li>Model detail pages consolidate all analysis for a single model, eliminating the need to jump between sections</li>
                                <li>Sub-tabs separate different types of analysis (explainability vs. bias vs. drift) while keeping them contextually grouped</li>
                                <li>Persistent context: The model header remains visible across sub-tabs so users never lose sight of which model they're analyzing</li>
                            </ul>
                            <div class="bg-pattern bg-img opacity-1"
